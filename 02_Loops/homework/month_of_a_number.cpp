#include<iostream>
using namespace std;

int main(){
    int n; 
    cout<<"Enter the number: "; 
    cin>>n;

    if(n==1){
        cout<<"Jan"<<endl; 
    }
    else if(n==2){
        cout<<"Feb"<<endl; 
    }
    else if(n==3){
        cout<<"Mar"<<endl;
    }
    else if(n==4){
        cout<<"Apr"<<endl; 
    }
    else if(n==5){
        cout<<"May"<<endl;
    }
    else if(n==6){
        cout<<"Jun"<<endl;
    }
    else if(n==7){
        cout<<"Jul"<<endl;
    }
    else if(n==8){
        cout<<"Aug"<<endl; 
    }
    else if(n==9){
        cout<<"Sep"<<endl; 
    }
    else if(n==10){
        cout<<"Oct"<<endl; 
    }
    else if(n==11){
        cout<<"Nov"<<endl; 
    }
    else if(n==12){
        cout<<"Dec"<<endl; 
    }
    else{
        cout<<"Not Valid"<<endl; 
    }
}